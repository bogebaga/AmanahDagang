				<h3>Tentang Kami</h3>
				<p>Harianamanah merupakan situs berita berkonten Islami yang berada di bawah naungan alHARAM Media Group, salah satu unit bisnis dari Abu Corp.</p>
				<p>Dikelola secara profesional oleh sumber daya yang berkompeten di bidang kejurnalistikan dengan bermodalkan semangat keislaman dalam menyampaikan kabar berita bernafaskan Islam lewat ranah maya.</p>
				<p>Portal berita Islami ini juga menjadi pelengkap dari media lainnya yang masih dibawahi alHARAM Media Group, masing-masing Koran Amanah Makassar, Koran Amanah Jakarta, Koran Amanah Semarang, dengan sub biro Kendari, Balikpapan, Bandung, Palembang, Medan, Surabaya, Solo, dan Jogjakarta.</p>
				<p>Mengakses  Harianamanah, pastikan diri Anda telah memperoleh wawasan keislaman dan menyerap informasi yang menyehatkan, sebab situs berita ini sama sekali tidak mengakomodir berita bermuatan gosip, apatah lagi bernilai fitnah yang sifatnya berpotensi mengadu domba.</p>
